
public class TestaPontoFlutuante {
	
	public static void main (String[] args) {
		
		//Variável double é usada em valores com decimais.
		double salario;
		salario = 1500.50;
		System.out.println("Meu salario é: " + salario);
		
		double divisao;
		divisao = 3.14 / 2;
		System.out.println("A divisão é: " + divisao);
		
		//Dividir números inteiros gera como resultado um inteiro
		int outraDivisao;
		outraDivisao = 5 / 2;
		System.out.println("A divisão é: " + outraDivisao);
		
		//Dividir números doubles
		double novaTentativa;
		novaTentativa = 5.0 / 2; 
		System.out.println("A divisão é: " + novaTentativa);
		
	}

}
